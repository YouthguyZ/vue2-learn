/**
 * @param {string} path
 * @returns {Boolean}
 */
export function isExternal(path) {
  return /^(https?:|mailto:|tel:)/.test(path)
}

/**
 * @param {string} str
 * @returns {Boolean}
 */
export function validMobile(str) {
  const reg = /^1[3-9]\d{9}$/ // 校验正则
  return reg.test(str)
}
